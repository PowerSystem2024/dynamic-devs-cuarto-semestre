# profundizando en tipos de dato float
a = 1.0
print(f"a = {a:.2f}")  # Formato con dos decimales

# constructores de float
b = float(2)
print(f"b = {b:.2f}")
c = float("3.14")
print(f"c = {c:.2f}")
d = float("4.2e-1")  # Notación científica
print(f"d = {d:.2f}")

# notacion exponencial
e = 3e5
print(f"e = {e:.2f}")
f = 3e-5
print(f"f = {f:.5f}")